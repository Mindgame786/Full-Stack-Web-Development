//---------------------------------------------------------------------------------------------                              Controlled Component in React                                  ---------------------------------------------------------------------------------------------

// A Controlled Component is  an input element whose value is managed by the React state using the useState() hook (or class components state).


//  Benefits of  controlled components?

// Better control over form data
// Easy validation (Before Submit)
// One source of truth (React state)
// Dynamic rendering based on user input (Due to state)
// Value attribute is attached with state.

